import { supabase } from "@/integrations/supabase/client";
import { CartItem, Product } from "@/lib/constants";
import { getCookieIdHash, getUserAgent } from "@/lib/identitySignals";
import { logSystemEvent, logSystemEventFailed } from "@/lib/systemEventService";
import { recordStockoutAttempt, type RecordStockoutInput } from "@/lib/stockoutService";

interface OrderInput {
  customerName: string;
  customerPhone: string;
  city?: string;
  region?: string;
  addressLine1?: string;
  isTbilisi?: boolean;
  items: CartItem[];
  subtotal: number;
  shippingFee?: number;
  total: number;
  source?: string;
  landingSlug?: string;
  status?: string;
}

interface SubmitOrderInput {
  order: OrderInput;
  stockout?: RecordStockoutInput;
  debugLabel?: string;
}

type SubmitOrderResult =
  | { kind: "order"; order: Awaited<ReturnType<typeof createOrder>> }
  | { kind: "stockout"; attemptId: string | null; recorded: boolean; originalError: unknown; recordError?: unknown };

function isOutOfStockError(err: unknown): err is Error {
  return err instanceof Error && err.message.startsWith("OUT_OF_STOCK");
}

/** Server-side OOS check: verify all items are in stock before creating order.
 * Considers BOTH the override table and the products.available flag so a product
 * marked unavailable directly on the catalog row is also blocked + tracked. */
async function checkStockAvailability(items: CartItem[]): Promise<string[]> {
  const productIds = items.map((i) => i.product.id);
  if (productIds.length === 0) return [];

  const [{ data: overrides }, { data: products }] = await Promise.all([
    supabase.from("product_stock_overrides").select("product_id, available").in("product_id", productIds),
    supabase.from("products").select("id, title, available").in("id", productIds),
  ]);

  const oos = new Set<string>();
  const titleById = new Map<string, string>();
  for (const p of products || []) {
    titleById.set(p.id, p.title);
    if (p.available === false) oos.add(p.id);
  }
  for (const o of overrides || []) {
    if (o.available === false) oos.add(o.product_id);
    // explicit override=true wins over a stale products.available=false
    if (o.available === true) oos.delete(o.product_id);
  }

  return Array.from(oos).map((id) => {
    const item = items.find((i) => i.product.id === id);
    return item?.product.title || titleById.get(id) || id;
  });
}

export async function createOrder(input: OrderInput) {
  const cookieIdHash = getCookieIdHash();
  const userAgent = getUserAgent();

  // ── Address inheritance: if city or address is missing, inherit from the
  //    customer's most recent order (matched by phone last 9 digits).
  //    Operator manual edits after insert are never touched (runs pre-insert only).
  let inheritedCity = input.city || "";
  let inheritedRegion = input.region || "";
  let inheritedAddress = input.addressLine1 || "";
  let inheritedIsTbilisi = input.isTbilisi ?? false;
  let addressInherited = false;
  const needsInheritance =
    (!inheritedCity.trim() || !inheritedAddress.trim()) && !!input.customerPhone;
  if (needsInheritance) {
    try {
      const digits = input.customerPhone.replace(/\D/g, "");
      const last9 = digits.slice(-9);
      if (last9.length >= 6) {
        const { data: prev } = await supabase
          .from("orders")
          .select("city, region, address_line1, is_tbilisi")
          .ilike("customer_phone", `%${last9}%`)
          .not("city", "is", null)
          .neq("city", "")
          .not("address_line1", "is", null)
          .neq("address_line1", "")
          .neq("status", "merged")
          .order("created_at", { ascending: false })
          .limit(1)
          .maybeSingle();
        if (prev) {
          if (!inheritedCity.trim() && prev.city) {
            inheritedCity = prev.city;
            inheritedRegion = prev.region || prev.city;
            inheritedIsTbilisi = !!prev.is_tbilisi;
            addressInherited = true;
          }
          if (!inheritedAddress.trim() && prev.address_line1) {
            inheritedAddress = prev.address_line1;
            addressInherited = true;
          }
        }
      }
    } catch (e) {
      console.warn("address inheritance lookup failed", e);
    }
  }

  let orderId = "unknown";

  try {
    // SERVER-SIDE OOS CHECK: Block order if any item is out of stock
    const oosItems = await checkStockAvailability(input.items);
    if (oosItems.length > 0) {
      const error = new Error(`OUT_OF_STOCK: ${oosItems.join(", ")}`);
      (error as any).oosItems = oosItems;
      throw error;
    }

    // Insert order — public_order_number is auto-generated by trigger when empty string
    const orderSource = input.source || "lovable";
    const orderStatus = input.status || "new";
    const { data: order, error: orderError } = await supabase
      .from("orders")
      .insert({
        customer_name: input.customerName,
        customer_phone: input.customerPhone,
        city: inheritedCity,
        region: inheritedRegion,
        address_line1: inheritedAddress,
        is_tbilisi: inheritedIsTbilisi,
        subtotal: input.subtotal,
        total: input.total,
        status: orderStatus,
        payment_method: "COD",
        public_order_number: "",
        raw_city: inheritedCity,
        raw_address: inheritedAddress,
        cookie_id_hash: cookieIdHash,
        user_agent: userAgent,
        source: orderSource,
        shipping_fee: input.shippingFee ?? 0,
        ...(addressInherited && orderStatus !== "pending_details"
          ? { address_status: "completed" }
          : {}),
        tags: [
          ...(input.landingSlug ? [`landing:${input.landingSlug}`] : []),
          ...(addressInherited ? ["address:inherited"] : []),
        ],
      } as any)
      .select("id, public_order_number")
      .single();

    if (orderError) throw orderError;
    orderId = order.id;

    // Insert order items
    const itemRows = input.items.map((item) => ({
      order_id: order.id,
      product_id: item.product.id,
      sku: item.product.sku || item.product.id,
      title: item.product.title,
      quantity: item.quantity,
      unit_price: item.product.price,
      line_total: item.product.price * item.quantity,
      image_url: item.product.image || "",
    }));

    const { error: itemsError } = await supabase
      .from("order_items")
      .insert(itemRows);

    if (itemsError) throw itemsError;

    // Create initial event
    await supabase.from("order_events").insert({
      order_id: order.id,
      actor: "system",
      event_type: "status_change",
      payload: { from: null, to: orderStatus, source: "storefront_checkout" },
    });

    // Log SUCCESS system event
    await logSystemEvent({
      entityType: "order",
      entityId: order.id,
      eventType: "ORDER_CREATE",
      actorId: "storefront",
      payload: {
        public_order_number: order.public_order_number,
        total: input.total,
        item_count: input.items.length,
      },
    });

    // Fire-and-forget: normalize and score (skip for pending_details)
    if (orderStatus !== "pending_details") {
      try {
        const supabaseUrl = import.meta.env.VITE_SUPABASE_URL;
        const supabaseKey = import.meta.env.VITE_SUPABASE_PUBLISHABLE_KEY;
        fetch(`${supabaseUrl}/functions/v1/normalize-and-score`, {
          method: "POST",
          headers: {
            "Content-Type": "application/json",
            "apikey": supabaseKey,
            "Authorization": `Bearer ${supabaseKey}`,
          },
          body: JSON.stringify({ order_id: order.id }),
          keepalive: true,
        });
      } catch (e) {
        console.warn("normalize-and-score call failed:", e);
      }
    }

    return order;
  } catch (err: any) {
    // Log FAILED system event
    await logSystemEventFailed({
      entityType: "order",
      entityId: orderId,
      eventType: "ORDER_CREATE",
      actorId: "storefront",
      errorMessage: err?.message || String(err),
      payload: { customer_phone: input.customerPhone, total: input.total },
    });
    throw err;
  }
}

/** Re-check live stock for a single product. Used as a hard guard so we never
 *  insert a stockout_attempt for a product that is actually in stock. */
async function isProductCurrentlyOutOfStock(productId: string): Promise<{
  oos: boolean;
  productAvailable: boolean | null;
  overrideAvailable: boolean | null;
}> {
  const [{ data: p }, { data: o }] = await Promise.all([
    supabase.from("products").select("available").eq("id", productId).maybeSingle(),
    supabase.from("product_stock_overrides").select("available").eq("product_id", productId).maybeSingle(),
  ]);
  const productAvailable = (p?.available ?? null) as boolean | null;
  const overrideAvailable = (o?.available ?? null) as boolean | null;
  let oos = false;
  if (productAvailable === false) oos = true;
  if (overrideAvailable === false) oos = true;
  if (overrideAvailable === true) oos = false; // explicit override wins
  return { oos, productAvailable, overrideAvailable };
}

export async function submitCustomerOrder(input: SubmitOrderInput): Promise<SubmitOrderResult> {
  try {
    const order = await createOrder(input.order);
    return { kind: "order", order };
  } catch (err) {
    if (!isOutOfStockError(err) || !input.stockout) throw err;

    const label = input.debugLabel || "Order submit";
    const productId = input.stockout.productId;

    // HARD GUARD: re-verify the product is actually out of stock right now.
    // Without this check a stale error, race, or unrelated failure could pollute
    // the Stockout Demand dashboard with in-stock products.
    const stock = await isProductCurrentlyOutOfStock(productId);
    const stockStatus = stock.oos ? "out_of_stock" : "in_stock";

    console.log(`[stockout-guard] ${label}`, {
      productId,
      handle: input.stockout.productHandle,
      sku: input.stockout.sku,
      productAvailable: stock.productAvailable,
      overrideAvailable: stock.overrideAvailable,
      isOutOfStock: stock.oos,
      action: stock.oos ? "insert" : "skip",
    });

    if (!stock.oos) {
      console.warn(
        `[stockout-guard] ${label}: OUT_OF_STOCK error raised but product is currently in stock — skipping stockout_attempt insert and re-throwing.`,
      );
      throw err;
    }

    try {
      const attempt = await recordStockoutAttempt({
        ...input.stockout,
        blockedReason: "out_of_stock",
        stockStatusAtAttempt: stockStatus,
      });
      console.log(`[stockout-guard] ${label}: stockout_attempt insert success`, attempt);
      return {
        kind: "stockout",
        attemptId: attempt.id,
        recorded: true,
        originalError: err,
      };
    } catch (recordError) {
      console.error(`[stockout-guard] ${label}: stockout_attempt insert failed:`, recordError);
      return {
        kind: "stockout",
        attemptId: null,
        recorded: false,
        originalError: err,
        recordError,
      };
    }
  }
}

/** Add upsell items to an existing order and update totals */
export async function addUpsellItems(
  orderId: string,
  items: { product: Product; quantity: number }[],
  newShippingFee: number,
  newTotal: number
) {
  // Insert additional order_items
  const itemRows = items.map((item) => ({
    order_id: orderId,
    product_id: item.product.id,
    sku: item.product.sku || item.product.id,
    title: item.product.title,
    quantity: item.quantity,
    unit_price: item.product.price,
    line_total: item.product.price * item.quantity,
    image_url: item.product.image || "",
    tags: ["upsell"],
  }));

  const { error: itemsError } = await supabase
    .from("order_items")
    .insert(itemRows);

  if (itemsError) throw itemsError;

  // Update order totals
  const { error: updateError } = await supabase
    .from("orders")
    .update({
      shipping_fee: newShippingFee,
      total: newTotal,
      subtotal: newTotal - newShippingFee,
    } as any)
    .eq("id", orderId);

  if (updateError) throw updateError;
}

/** Update order with address details and finalize status.
 *  `addressStatus`: 'completed' when both city + address are present, 'partial' when only city. */
export async function updateOrderAddress(
  orderId: string,
  data: {
    city: string;
    region: string;
    addressLine1: string;
    isTbilisi: boolean;
    addressStatus?: "completed" | "partial";
  }
) {
  const status = data.addressStatus || (data.addressLine1.trim() ? "completed" : "partial");
  const { error } = await supabase
    .from("orders")
    .update({
      city: data.city,
      region: data.region,
      address_line1: data.addressLine1,
      raw_city: data.city,
      raw_address: data.addressLine1,
      is_tbilisi: data.isTbilisi,
      status: "new",
      address_status: status,
      address_added_at: new Date().toISOString(),
      skipped_address: false,
    } as any)
    .eq("id", orderId);

  if (error) throw error;

  try {
    const supabaseUrl = import.meta.env.VITE_SUPABASE_URL;
    const supabaseKey = import.meta.env.VITE_SUPABASE_PUBLISHABLE_KEY;
    fetch(`${supabaseUrl}/functions/v1/normalize-and-score`, {
      method: "POST",
      headers: {
        "Content-Type": "application/json",
        "apikey": supabaseKey,
        "Authorization": `Bearer ${supabaseKey}`,
      },
      body: JSON.stringify({ order_id: orderId }),
      keepalive: true,
    });
  } catch (e) {
    console.warn("normalize-and-score call failed:", e);
  }
}

/** Mark that the customer explicitly skipped the address popup. Does not delete the order. */
export async function markOrderAddressSkipped(orderId: string) {
  const { error } = await supabase
    .from("orders")
    .update({
      skipped_address: true,
      address_status: "missing",
    } as any)
    .eq("id", orderId);
  if (error) throw error;
}

